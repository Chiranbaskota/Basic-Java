public class pattern_printing
{
public static void main(String[] args)
{

    // for solid rectangle

    for(int i=1; i<=4; i++){
       
        for(int j=1; j<=5; j++){
            System.out.print("*");
        }
        System.out.println(); // This line is used to display remaining output in next line.
    }
//System.out.print(); only writing this line brings syntax error.


      


}
}